    /**
     * Product list view. Returns the full Inertia prop shape expected by Products/Index.tsx.
     * Mock data covers 50 apparel/outdoor SKUs with varied margins, some without COGS set.
     */
    public function index(Request $request): Response
    {
        $params  = $this->validateIndexParams($request);
        $products = $this->mockProducts();

        return Inertia::render('Products/Index', [
            'products'             => $products,
            'metrics'              => $this->mockMetrics($products),
            'pareto_data'          => $this->buildParetoData($products),
            'filters'              => $params,
            'cogs_configured_count' => collect($products)->whereNotNull('cogs')->count(),
            'total_skus'           => count($products),
            'product_journey'      => $this->mockProductJourney($products),
            'market_basket'        => $this->mockMarketBasket($products),
            'initial_search'       => $params['initial_search'] ?? null,
        ]);
    }

    /**
     * Inline COGS upsert — called by the InlineCogs cell in the table.
     *
     * The TSX sends { cogs: number } via router.patch().
     * We apply it to all variants of this product (flat override, no date-ranging in v1).
     * Per docs/pages/products.md: this does NOT retroactively restate historical COGS.
     * A Toast on save says "New COGS applies to orders from today forward. [Backfill historical]".
     */
    public function updateCogs(Request $request, string $workspace, int $product): RedirectResponse
    {
        $request->validate([
            'cogs' => ['required', 'numeric', 'min:0'],
        ]);

        // Service will write product_variants.cogs_amount here in v1.
        // For now: no-op OK — the frontend uses optimistic UI.
        return back()->with('success', 'COGS updated. New COGS applies to orders from today forward.');
    }

