/**
 * Products page — workspace-scoped product table + inline COGS edit.
 *
 * Thin controller: all aggregation will be delegated to ProductsDataService once
 * the service layer is built. Until then, returns mock prop data so the
 * frontend is fully renderable without any DB dependency.
 *
 * Prop shape matches Products/Index.tsx exactly:
 *   products, metrics, pareto_data, filters, cogs_configured_count,
 *   total_skus, product_journey, market_basket, initial_search
 *
 * Reads: daily_snapshot_products, products, product_variants (future service).
 * Writes: product_variants.cogs_amount (inline COGS update).
 * Called by:
 *   - GET   /{workspace:slug}/products           → index()
 *   - PATCH /{workspace:slug}/products/{id}/cogs → updateCogs()
 *
 * @see docs/pages/products.md
 * @see docs/planning/backend.md#ProductsDataService
 */
class ProductsController extends Controller
