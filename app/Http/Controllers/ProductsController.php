    /** @return array<string, mixed> */
    private function validateIndexParams(Request $request): array
    {
        $v = $request->validate([
            'from'        => ['sometimes', 'nullable', 'date_format:Y-m-d'],
            'to'          => ['sometimes', 'nullable', 'date_format:Y-m-d'],
            'country'     => ['sometimes', 'nullable', 'string', 'max:3'],
            'channel'     => ['sometimes', 'nullable', 'string', 'max:80'],
            'sort'        => ['sometimes', 'nullable', 'in:revenue,margin,velocity,orders,units,name,aov,repeat_rate'],
            'stock_alert' => ['sometimes', 'nullable', 'in:7,30'],
            'search'      => ['sometimes', 'nullable', 'string', 'max:255'],
        ]);

        return [
            'from'           => $v['from']        ?? now()->subDays(27)->toDateString(),
            'to'             => $v['to']          ?? now()->toDateString(),
            'country'        => $v['country']     ?? null,
            'channel'        => $v['channel']     ?? null,
            'sort'           => $v['sort']        ?? 'revenue',
            'stock_alert'    => isset($v['stock_alert']) ? (int) $v['stock_alert'] : null,
            'initial_search' => $v['search']      ?? null,
        ];
    }

    /**
     * Compute summary metrics from the products array.
     *
     * @param  array<int, array<string, mixed>> $products
     * @return array<string, mixed>
     */
    private function mockMetrics(array $products): array
    {
        $withCogs   = array_filter($products, fn ($p) => $p['cogs'] !== null);
        $margins    = array_map(fn ($p) => $p['margin_pct'], $withCogs);
        $velocities = array_filter(array_map(fn ($p) => $p['velocity'], $products));

        sort($margins);
        sort($velocities);

        $median = fn (array $arr) => count($arr) > 0
            ? (count($arr) % 2 === 0
                ? ($arr[count($arr) / 2 - 1] + $arr[count($arr) / 2]) / 2
                : $arr[(int) (count($arr) / 2)])
            : null;

        $revenues = array_column($products, 'revenue');
        arsort($revenues);
        $totalRev = array_sum($revenues);
        $top10    = array_sum(array_slice($revenues, 0, 10));
        $top10pct = $totalRev > 0 ? round($top10 / $totalRev * 100, 1) : null;

        $velocityArr = array_values($velocities);
        sort($velocityArr);

        return [
            'products_sold'         => count($products),
            'top10_concentration'   => $top10pct,
            'median_gross_margin'   => $median(array_values($margins)),
            'velocity_mean'         => count($velocityArr) > 0 ? round(array_sum($velocityArr) / count($velocityArr), 2) : null,
            'velocity_median'       => $median($velocityArr),
            'velocity_mode'         => count($velocityArr) > 0 ? $velocityArr[0] : null,
        ];
    }

    /**
     * Build Pareto data (revenue DESC, cumulative %) for the Pareto chart.
     *
     * @param  array<int, array<string, mixed>> $products
     * @return array<int, array<string, mixed>>
     */
    private function buildParetoData(array $products): array
    {
        $sorted = collect($products)
            ->sortByDesc('revenue')
            ->values();

        $total = $sorted->sum('revenue') ?: 1;
        $cum   = 0.0;

        return $sorted->map(function ($p, $i) use ($total, &$cum) {
            $cum += $p['revenue'] / $total * 100;
            return [
                'rank'           => $i + 1,
                'name'           => $p['name'],
                'revenue'        => $p['revenue'],
                'cumulative_pct' => round($cum, 2),
            ];
        })->values()->all();
    }

    /**
     * Product journey mock — which SKU appears as 1st / 2nd / 3rd purchase.
     *
     * @param  array<int, array<string, mixed>> $products
     * @return array<int, array<string, mixed>>
     */
    private function mockProductJourney(array $products): array
    {
        return collect($products)
            ->take(12)
            ->map(fn ($p, $i) => [
                'product_external_id' => $p['external_id'],
                'product_name'        => $p['name'],
                'as_first'            => max(0, (int) round($p['orders'] * 0.62)),
                'as_second'           => max(0, (int) round($p['orders'] * 0.22)),
                'as_third'            => max(0, (int) round($p['orders'] * 0.10)),
            ])
            ->values()
            ->all();
    }

    /**
     * Market basket mock — frequently bought together pairs.
     *
     * @param  array<int, array<string, mixed>> $products
     * @return array<int, array<string, mixed>>
     */
    private function mockMarketBasket(array $products): array
    {
        $pairs = [];
        $slice = array_slice($products, 0, 8);
        for ($i = 0; $i < count($slice) - 1; $i++) {
            for ($j = $i + 1; $j < count($slice); $j++) {
                $pairs[] = [
                    'product_a_id'      => $slice[$i]['external_id'],
                    'product_a'         => $slice[$i]['name'],
                    'product_b_id'      => $slice[$j]['external_id'],
                    'product_b'         => $slice[$j]['name'],
                    'co_purchase_count' => rand(8, 84),
                    'lift_pct'          => round(rand(30, 180) / 10, 1),
                ];
            }
        }
        // Limit to 10 pairs sorted by co_purchase_count DESC
        usort($pairs, fn ($a, $b) => $b['co_purchase_count'] - $a['co_purchase_count']);
        return array_slice($pairs, 0, 10);
    }

